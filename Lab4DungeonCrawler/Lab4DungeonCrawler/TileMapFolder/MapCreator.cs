        public List<TileType> CreateMap()
        {
            Point point;
            for (int y = 0; y < map.GetLength(1); y++)
            {
                for (int x = 0; x < map.GetLength(0); x++)
                {
                    if (map[x, y] == '#')
                    {
                        point = new Point(x, y);
                        gameObjects.Add(new WallTile(point));
                    }

                    else if (map[x, y] == 'D')
                    {
                        point = new Point(x, y);
                        gameObjects.Add(new DoorTile(point));
                    }
                    else
                    {
                        point = new Point(x, y);
                        gameObjects.Add(new FloorTile(point));
                    }
                }
            }
            return gameObjects;
        }
